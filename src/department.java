public class department
{
    int srno;
    String dept_name;

    public department (int srno,String dept_name)
    {

        this.srno=srno;
        this.dept_name=dept_name;

    }

    public int getSrno()
    {
        return srno;
    }
    public String getDept_name()
    {
        return dept_name;
    }


}
